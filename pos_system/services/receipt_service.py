def generate_receipt_pdf(order_id, lang='en'):
    d = get_order_data(order_id)
    if not d:
        return None

    data = d['_data']
    lines = d['lines']
    payments = d['payments']
    company = d['company']
    currency = d.get('currency', {})

    pdf = ReceiptPDF()
    pdf.add_page()
    pdf.set_margins(5, 5, 5)

    page_w = 80 - 10
    left = 5

    y = 5

    # Logo
    if company.get('logo'):
        try:
            img_data = base64.b64decode(company['logo'])
            img_fp = io.BytesIO(img_data)
            pdf.image(img_fp, x=left + page_w / 2 - 15, w=30, h=15)
            y = pdf.get_y() + 2
        except Exception:
            pass

    # Company name
    pdf.set_y(max(y, pdf.get_y()))
    pdf.set_font('Courier', 'B', 10)
    pdf.set_x(left)
    pdf.cell(page_w, 5, company.get('name', ''), align='C')
    pdf.ln(5)

    # Company info
    pdf.set_font('Courier', '', 7)
    info_lines = []
    if company.get('street'):
        info_lines.append(company['street'])
    city_parts = []
    if company.get('city'):
        city_parts.append(company['city'])
    if company.get('state'):
        city_parts.append(company['state'])
    if company.get('zip_code'):
        city_parts.append(company['zip_code'])
    if city_parts:
        info_lines.append(' '.join(city_parts))
    if company.get('country'):
        info_lines.append(company['country'])
    if company.get('phone'):
        info_lines.append(f"{_('tel', lang)}: {company['phone']}")
    if company.get('email'):
        info_lines.append(company['email'])
    if company.get('vat'):
        info_lines.append(f"{_('tax_id', lang)}: {company['vat']}")

    for line in info_lines:
        pdf.set_x(left)
        pdf.cell(page_w, 3.5, line, align='C')
        pdf.ln(3.5)

    # Dashed line
    pdf.ln(1)
    pdf.set_x(left)
    pdf.set_font('Courier', '', 8)
    pdf.cell(page_w, 3, '-' * 70, align='C')
    pdf.ln(4)

    # Receipt header
    pdf.set_font('Courier', 'B', 12)
    pdf.set_x(left)
    pdf.cell(page_w, 6, _('receipt', lang).upper(), align='C')
    pdf.ln(6)

    # Dashed line
    pdf.set_x(left)
    pdf.set_font('Courier', '', 8)
    pdf.cell(page_w, 3, '-' * 70, align='C')
    pdf.ln(4)

    ref = data.get('name', '') or f"{_('order', lang)} #{d['id']}"
    date_val = (data.get('date_order', '') or '')[:19]
    state = data.get('state', '')
    total = float(data.get('amount_total', 0) or 0)
    delivery_cost = float(d.get('delivery_cost', 0) or 0)
    items_total = total - delivery_cost
    paid = float(data.get('amount_paid', 0) or 0)
    change = float(data.get('amount_change', 0) or 0)

    # Order info
    pdf.set_font('Courier', '', 8)
    pdf.set_x(left)
    pdf.cell(page_w, 4, f"{_('order', lang)}: {ref}  {_('status', lang)}: {state.upper()}")
    pdf.ln(4)
    pdf.set_x(left)
    pdf.cell(page_w, 4, f"{_('date', lang)}: {date_val}")
    pdf.ln(4)
    pdf.set_x(left)
    pdf.cell(page_w, 4, f"{_('cashier', lang)}: {d['user_name']}")
    pdf.ln(4)
    pdf.set_x(left)
    pdf.cell(page_w, 4, f"{_('customer', lang)}: {d['partner_name']}")
    pdf.ln(4)

    if d.get('delivery_contact_name') or d.get('delivery_contact_phone'):
        parts = []
        if d.get('delivery_contact_name'):
            parts.append(d['delivery_contact_name'])
        if d.get('delivery_contact_phone'):
            parts.append(d['delivery_contact_phone'])
        contact_str = ' - '.join(parts)
        dz_name = d.get('delivery_zone_name', '')
        pdf.set_x(left)
        pdf.cell(page_w, 4, f"{_('delivery', lang)}: {dz_name}  {contact_str}")
        pdf.ln(4)

    # Dashed line
    pdf.set_x(left)
    pdf.set_font('Courier', '', 8)
    pdf.cell(page_w, 3, '-' * 70, align='C')
    pdf.ln(4)

    # Items header
    pdf.set_font('Courier', 'B', 8)
    col_w = [28, 8, 18, 16]
    headers = [_('item', lang), _('qty', lang), _('price', lang), _('total', lang)]
    pdf.set_x(left)
    for i, h in enumerate(headers):
        pdf.cell(col_w[i], 4, h)
    pdf.ln(4)

    pdf.set_font('Courier', '', 8)
    pdf.set_x(left)
    pdf.cell(page_w, 2, '-' * 70, align='C')
    pdf.ln(3)

    # Items
    for line in lines:
        ld = line['_data']
        name = line['product_name']
        qty = float(ld.get('qty', 0) or 0)
        price = float(ld.get('price_unit', 0) or 0)
        disc = float(ld.get('discount', 0) or 0)
        subtotal = float(ld.get('price_subtotal', 0) or (qty * price * (1 - disc / 100)))

        pdf.set_x(left)
        pdf.cell(col_w[0], 4, name[:18])
        pdf.cell(col_w[1], 4, str(int(qty) if qty == int(qty) else qty), align='C')
        pdf.cell(col_w[2], 4, currency_format(price, currency), align='R')
        pdf.cell(col_w[3], 4, currency_format(subtotal, currency), align='R')
        pdf.ln(4)

    # Dashed line
    pdf.set_x(left)
    pdf.set_font('Courier', '', 8)
    pdf.cell(page_w, 3, '-' * 70, align='C')
    pdf.ln(4)

    # Totals
    pdf.set_font('Courier', '', 8)
    pdf.set_x(left)
    pdf.cell(page_w - 20, 4, f"{_('subtotal', lang)}:", align='R')
    pdf.cell(20, 4, currency_format(items_total, currency), align='R')
    pdf.ln(4)

    if delivery_cost > 0:
        pdf.set_x(left)
        pdf.cell(page_w - 20, 4, f"{_('delivery', lang)}:", align='R')
        pdf.cell(20, 4, currency_format(delivery_cost, currency), align='R')
        pdf.ln(4)

    pdf.set_x(left)
    pdf.cell(page_w - 20, 4, f"{_('discount', lang)}:", align='R')
    pdf.cell(20, 4, currency_format(0, currency), align='R')
    pdf.ln(4)

    pdf.set_font('Courier', 'B', 9)
    pdf.set_x(left)
    pdf.cell(page_w - 20, 5, f"{_('total', lang).upper()}:", align='R')
    pdf.cell(20, 5, currency_format(total, currency), align='R')
    pdf.ln(6)

    # Receipt header text
    if company.get('receipt_header'):
        pdf.set_x(left)
        pdf.set_font('Courier', '', 7)
        pdf.cell(page_w, 3, '-' * 70, align='C')
        pdf.ln(3)
        pdf.set_x(left)
        pdf.cell(page_w, 4, company['receipt_header'], align='C')
        pdf.ln(4)

    # Payment info
    pdf.set_x(left)
    pdf.set_font('Courier', '', 8)
    pdf.cell(page_w, 3, '-' * 70, align='C')
    pdf.ln(3)
    pdf.set_font('Courier', 'B', 8)
    pdf.set_x(left)
    pdf.cell(page_w, 4, _('payment', lang), align='C')
    pdf.ln(5)

    pdf.set_font('Courier', '', 8)
    for pmt in payments:
        pd = pmt['_data']
        pmt_name = pmt['payment_method_name']
        amt = float(pd.get('amount', 0) or 0)
        pdf.set_x(left)
        pdf.cell(page_w - 20, 4, pmt_name, align='L')
        pdf.cell(20, 4, currency_format(amt, currency), align='R')
        pdf.ln(4)

    pdf.set_x(left)
    pdf.cell(page_w - 20, 4, f"{_('change', lang)}:", align='R')
    pdf.cell(20, 4, currency_format(change, currency), align='R')
    pdf.ln(4)

    # Receipt footer
    if company.get('receipt_footer'):
        pdf.set_x(left)
        pdf.set_font('Courier', '', 8)
        pdf.cell(page_w, 3, '-' * 70, align='C')
        pdf.ln(3)
        pdf.set_font('Courier', '', 8)
        pdf.set_x(left)
        pdf.cell(page_w, 4, company['receipt_footer'], align='C')
        pdf.ln(4)

    # Footer line
    pdf.set_x(left)
    pdf.set_font('Courier', '', 7)
    pdf.cell(page_w, 3, '-' * 70, align='C')
    pdf.ln(3)
    pdf.set_x(left)
    pdf.cell(page_w, 3, _('generated', lang), align='C')

    # Return as bytes
    result = pdf.output(dest='S')
    if isinstance(result, bytearray):
        return bytes(result)
    if isinstance(result, str):
        return result.encode('latin-1')
    return result
