def get_order_data(order_id):
    orders = PosOrder().browse([order_id])
    if not orders:
        return None
    order = orders[0]
    d = {'id': order.id, '_data': order._data}

    lines = PosOrderLine().search([('order_id', '=', order.id)])
    d['lines'] = []
    for line in lines:
        ld = {'_data': line._data}
        pid = line._data.get('product_id', 0) or 0
        pname = 'Product'
        if pid:
            product = ProductProduct().browse([pid])
            if product:
                pname = product[0]._data.get('name', 'Product')
        ld['product_name'] = pname
        d['lines'].append(ld)

    payments = PosPayment().search([('order_id', '=', order.id)])
    d['payments'] = []
    for pmt in payments:
        pd_data = {'_data': pmt._data}
        pmid = pmt._data.get('payment_method_id', 0) or 0
        pname = 'Cash'
        if pmid:
            method = PosPaymentMethod().browse([pmid])
            if method:
                pname = method[0]._data.get('name', 'Cash')
        pd_data['payment_method_name'] = pname
        d['payments'].append(pd_data)

    pid = order._data.get('partner_id', 0) or 0
    d['partner_name'] = '-'
    if pid:
        partner = ResPartner().browse([pid])
        if partner:
            d['partner_name'] = partner[0]._data.get('name', '-')

    uid = order._data.get('user_id', 0) or 0
    d['user_name'] = ''
    if uid:
        user = ResUsers().browse([uid])
        if user:
            d['user_name'] = user[0]._data.get('name', '')

    dzid = order._data.get('delivery_zone_id', 0) or 0
    d['delivery_zone_name'] = ''
    d['delivery_cost'] = float(order._data.get('delivery_cost', 0) or 0)
    d['delivery_contact_name'] = order._data.get('delivery_contact_name', '') or ''
    d['delivery_contact_phone'] = order._data.get('delivery_contact_phone', '') or ''
    if dzid:
        zone = DeliveryZone().browse([dzid])
        if zone:
            d['delivery_zone_name'] = zone[0]._data.get('name', '')

    company = ResCompany().search([], limit=1)
    d['company'] = company[0]._data if company else {}
    if company:
        cur_id = company[0]._data.get('currency_id', 0) or 0
        if cur_id:
            cur = ResCurrency().browse([cur_id])
            if cur:
                d['currency'] = cur[0]._data

    return d


def currency_format(amount, currency=None):
    if currency is None:
        currency = {}
    symbol = currency.get('symbol', 'FG')
    decimals = currency.get('decimal_places', 0)
    position = currency.get('position', 'before')
    formatted = f"{float(amount or 0):.{decimals}f}"
    if position == 'before':
        return f"{symbol} {formatted}"
    return f"{formatted} {symbol}"


